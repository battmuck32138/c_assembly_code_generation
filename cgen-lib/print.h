// Used to print out strings.
int PrintString (char *str);

// Used to print out integers.
int PrintInt (int i);

// Used to print out character.
int PrintChar (char c);

// Prints bools as an integer
// value. Used solely for type
// matching.
int PrintBool (bool b);

// Prints out a newline
int PrintNewline ();

// Prints out many integers
int PrintInts (int a, int b, int c);

// Prints out many chars
int PrintChars (char a, char b, char c);

// Prints out chars and ints
int PrintMixed (char a, char b, int x, int y, char c, int z);
